package week6;

public class LearningDoWhileLoop {

	public static void main(String[] args) {
		int a = 10;
		do {
			System.out.println(a + ": Welcome to Java");
			a++;
		} while (a < 10);
		
		int b = 10;
		while(b < 10) {
			System.out.println(b + ": Programming Application");
			b++;
		}
	}
	
}
