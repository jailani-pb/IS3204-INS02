package week4;

public class LearningSpecialCharacters {

	public static void main(String[] args) {
		// Tab
		System.out.println("\tHello World");
		System.out.println("\t\t\tHello World");
		System.out.println("(1)\tPencil");
		System.out.println("(2)\tPen");
		System.out.println("(3)\tEraser");
		// Newline
		System.out.println("Hello World\n");
		System.out.println("Jailani\nRahman");
		// Printing double quote
		System.out.println("Hello \"World");
		// Printing backward slash
		System.out.println("Hello \\World");
	}
	
}
