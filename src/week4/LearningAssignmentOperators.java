package week4;

public class LearningAssignmentOperators {

	public static void main(String[] args) {
		// Add and Assign
		int a = 10;
		System.out.println(a);
		a += 5; // Same as a = a + 5
		System.out.println(a);
		// Subtract and Assign
		int b = 10;
		System.out.println(b);
		b -= 15; // Same as b = b - 15
		System.out.println(b);
		// Multiply and Assign
		int c = 10;
		System.out.println(c);
		c *= 3; // Same as c = c * 3
		System.out.println(c);
		// Divide and Assign
		int d = 15;
		System.out.println(d);
		d /= 5; // Same as d = d / 5
		System.out.println(d);
	}
	
}
